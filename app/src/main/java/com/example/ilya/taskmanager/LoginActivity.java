public class LoginActivity extends AppCompatActivity  {

    private static final String TAG = RegisterActivity.class.getSimpleName();
    private Button btnLogin;
    private Button btnLinkToRegister;
    private EditText inputEmail;
    private EditText inputPassword;
    private ProgressDialog pDialog;
    private TextInputLayout emailWrapperLogin;
    private TextInputLayout passwordWrapperLogin;


    private static final String EMAIL_PATTERN = "^[a-zA-Z0-9#_~!$&'()*+,;=:.\"(),:;<>@\\[\\]\\\\]+@[a-zA-Z0-9-]+(\\.[a-zA-Z0-9-]+)*$";
    private Pattern pattern = Pattern.compile(EMAIL_PATTERN);
    private Matcher matcher;

    private ProgressDialog progressDialog;
    private FirebaseAuth firebaseAuth;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_login);

        firebaseAuth= FirebaseAuth.getInstance();
        if(firebaseAuth.getCurrentUser()!=null){
            startActivity(new Intent(LoginActivity.this, MainActivity.class));
            finish();
        }

        inputEmail = (EditText) findViewById(R.id.email);
        inputPassword = (EditText) findViewById(R.id.password);
        btnLogin = (Button) findViewById(R.id.btnLogin);
        btnLinkToRegister = (Button) findViewById(R.id.btnLinkToRegisterScreen);
        emailWrapperLogin=(TextInputLayout) findViewById(R.id.emailWrapperLogin);
        passwordWrapperLogin=(TextInputLayout)findViewById(R.id.passwordWrapperLogin);

        progressDialog=new ProgressDialog(this);

    }

    // Login button Click Event
    public void onClickLogin(View view){
        hideKeyboard();

        String email=inputEmail.getText().toString();
        String password=inputPassword.getText().toString();

        if (!validateEmail(email)) {
            emailWrapperLogin.setError("Not a valid email address!");
        } else if (!validatePassword(password)) {
            passwordWrapperLogin.setError("Not a valid password!");
        } else {
            emailWrapperLogin.setErrorEnabled(false);
            passwordWrapperLogin.setErrorEnabled(false);
            doLogin();
        }


    }

    // Link to Register Screen
    public void onClickRegister(View view){
        Intent i = new Intent(getApplicationContext(),
                RegisterActivity.class);
        startActivity(i);
        finish();

    }

    public boolean validateEmail(String email) {
        matcher = pattern.matcher(email);
        return matcher.matches();
    }
    public boolean validatePassword(String password) {
        return password.length() > 5;
    }
    private void hideKeyboard() {
        View view = getCurrentFocus();
        if (view != null) {
            ((InputMethodManager) getSystemService(Context.INPUT_METHOD_SERVICE)).
                    hideSoftInputFromWindow(view.getWindowToken(), InputMethodManager.HIDE_NOT_ALWAYS);
        }
    }
    public void doLogin() {
        String email=inputEmail.getText().toString();
        String password=inputPassword.getText().toString();


        firebaseAuth.signInWithEmailAndPassword(email,password)
                .addOnCompleteListener(this, new OnCompleteListener<AuthResult>() {
                    @Override
                    public void onComplete(@NonNull Task<AuthResult> task) {
                        if(!task.isSuccessful()){
                            Toast.makeText(LoginActivity.this, "Login failed." + task.getException(),
                                    Toast.LENGTH_LONG).show();
                        }else {
                            startActivity(new Intent(LoginActivity.this, MainActivity.class));
                            finish();
                        }
                    }
                });
    }


}

